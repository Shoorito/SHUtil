namespace SHUtil
{
    /// <summary>
    /// 스레드 안전한 제네릭 싱글톤 기반 클래스입니다.
    /// </summary>
    public class Singleton<T> where T : class, new()
    {
        private static readonly T sInstance = new T();

        public static T Instance => sInstance;
    }
}
